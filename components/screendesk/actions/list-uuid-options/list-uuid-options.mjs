import screendesk from "../../screendesk.app.mjs";

export default {
  key: "screendesk-list-uuid-options",
  name: "List UUID Options",
  description: "Retrieves available options for the UUID field.",
  version: "0.0.1",
  type: "action",
  annotations: {
    destructiveHint: false,
    openWorldHint: true,
    readOnlyHint: true,
  },
  props: {
    screendesk,
    page: {
      type: "integer",
      label: "Page",
      description: "The page of results to retrieve.",
      min: 0,
      default: 0,
    },
  },
  async run({ $ }) {
    const options = await screendesk.propDefinitions.uuid.options.call(this.screendesk, {
      page: this.page,
    });
    $.export("$summary", `Successfully retrieved ${options.length} option${options.length === 1
      ? ""
      : "s"}`);
    return options;
  },
};
