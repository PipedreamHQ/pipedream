import redmine from "../../redmine.app.mjs";

export default {
  key: "redmine-list-project-id-options",
  name: "List Project ID Options",
  description: "Retrieves available options for the Project ID field.",
  version: "0.0.1",
  type: "action",
  annotations: {
    destructiveHint: false,
    openWorldHint: true,
    readOnlyHint: true,
  },
  props: {
    redmine,
  },
  async run({ $ }) {
    const options = await redmine.propDefinitions.projectId.options.call(this.redmine, {});
    $.export("$summary", `Successfully retrieved ${options.length} option${
      options.length === 1
        ? ""
        : "s"
    }`);
    return options;
  },
};
