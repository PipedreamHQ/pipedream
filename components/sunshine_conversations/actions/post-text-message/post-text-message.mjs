import { ConfigurationError } from "@pipedream/platform";
import { parseObject } from "../../common/utils.mjs";
import sunshineConversations from "../../sunshine_conversations.app.mjs";

export default {
  key: "sunshine_conversations-post-text-message",
  name: "Post Text Message",
  description: "Post a text message. [See the documentation](https://developer.zendesk.com/api-reference/conversations/#tag/Messages/operation/PostMessage)",
  version: "0.0.1",
  annotations: {
    destructiveHint: false,
    openWorldHint: true,
    readOnlyHint: false,
  },
  type: "action",
  props: {
    sunshineConversations,
    userId: {
      propDefinition: [
        sunshineConversations,
        "userId",
      ],
    },
    conversationId: {
      propDefinition: [
        sunshineConversations,
        "conversationId",
        ({ userId }) => ({
          userId,
        }),
      ],
    },
    authorType: {
      type: "string",
      label: "Author Type",
      description: "The author type. Either \"user\" (representing the end user) or \"business\" (sent on behalf of the business)",
      options: [
        "business",
        "user",
      ],
    },
    subtypes: {
      type: "string[]",
      label: "Subtypes",
      description: "An array that indicates the author's subtypes",
      optional: true,
      options: [
        {
          label: "AI - Generated by AI (available in responses only)",
          value: "AI",
        },
        {
          label: "Activity - Generated by system activities",
          value: "activity",
        },
        {
          label: "Agent - Generated by human agents (available in responses only)",
          value: "agent",
        },
        {
          label: "Bot - Generated by bot agents (available in responses only)",
          value: "bot",
        },
      ],
    },
    displayName: {
      type: "string",
      label: "Display Name",
      description: "The display name of the message author",
      optional: true,
    },
    avatarUrl: {
      type: "string",
      label: "Avatar URL",
      description: "A custom message icon URL. The image must be JPG, PNG, or GIF format",
      optional: true,
    },
    text: {
      type: "string",
      label: "Text",
      description: "The text content of the message. Required unless actions, htmlText or markdownText is provided",
      optional: true,
    },
    htmlText: {
      type: "string",
      label: "HTML Text",
      description: "HTML text content of the message. Can be provided in place of `text`. Cannot be used with `markdownText`. If no `text` is provided, will be converted to `text` upon reception to be displayed on channels that do not support rich text. See [rich text](https://developer.zendesk.com/documentation/conversations/messaging-platform/programmable-conversations/structured-messages/#rich-text) documentation for more information",
      optional: true,
    },
    blockChatInput: {
      type: "boolean",
      label: "Block Chat Input",
      description: "When set to true, the chat input will be disabled on supported client implementations when the message is the most recent one in the history. Can be used for guided flows or to temporarily disable the user's ability to send messages in the conversation",
      optional: true,
    },
    markdownText: {
      type: "string",
      label: "Markdown Text",
      description: "Markdown text content of the message. Can be provided in place of `text`. Cannot be used with `htmlText`. Will be converted to `htmlText` upon reception. If converted `htmlText` exceeds 4096 characters, the message will be rejected. If no `text` is provided, will be converted to `text` upon reception to be displayed on channels that do not support rich text. See [rich text](https://developer.zendesk.com/documentation/conversations/messaging-platform/programmable-conversations/structured-messages/#rich-text) documentation for more information",
      optional: true,
    },
    payload: {
      type: "string",
      label: "Payload",
      description: "The payload of a [reply button](https://developer.zendesk.com/documentation/conversations/messaging-platform/programmable-conversations/structured-messages/#reply-buttons) response message",
      optional: true,
    },
    metadata: {
      type: "object",
      label: "Metadata",
      description: "Flat object containing custom properties. Strings, numbers and booleans are the only supported format that can be passed to metadata. The metadata is limited to 4KB in size",
      optional: true,
    },
  },
  async run({ $ }) {
    if (this.htmlText && this.markdownText) {
      throw new ConfigurationError("`htmlText` and `markdownText` cannot be used together");
    }

    if (!this.text && !this.htmlText && !this.markdownText) {
      throw new ConfigurationError("Either `text`, `htmlText`, or `markdownText` is required");
    }

    const author = {
      type: this.authorType,
      subtypes: parseObject(this.subtypes),
      displayName: this.displayName,
      avatarUrl: this.avatarUrl,
    };

    if (this.authorType === "user") {
      author.userId = this.userId;
    }

    const response = await this.sunshineConversations.postMessage({
      $,
      conversationId: this.conversationId,
      data: {
        author,
        content: {
          type: "text",
          text: this.text,
          htmlText: this.htmlText,
          blockChatInput: this.blockChatInput,
          markdownText: this.markdownText,
          payload: this.payload,
        },
        metadata: parseObject(this.metadata),
      },
    });

    $.export("$summary", `Successfully posted message with ID ${response.messages[0].id}`);
    return response;
  },
};
