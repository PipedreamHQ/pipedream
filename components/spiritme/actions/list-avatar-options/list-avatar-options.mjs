import spiritme from "../../spiritme.app.mjs";

export default {
  key: "spiritme-list-avatar-options",
  name: "List Avatar Options",
  description: "Retrieves available options for the Avatar field.",
  version: "0.0.1",
  type: "action",
  annotations: {
    destructiveHint: false,
    openWorldHint: true,
    readOnlyHint: true,
  },
  props: {
    spiritme,
    page: {
      type: "integer",
      label: "Page",
      description: "The page of results to retrieve.",
      min: 0,
      default: 0,
    },
  },
  async run({ $ }) {
    const options = await spiritme.propDefinitions.avatar.options.call(this.spiritme, {
      page: this.page,
    });
    $.export("$summary", `Successfully retrieved ${options.length} option${options.length === 1
      ? ""
      : "s"}`);
    return options;
  },
};
