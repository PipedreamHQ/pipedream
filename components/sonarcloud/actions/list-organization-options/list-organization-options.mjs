import sonarcloud from "../../sonarcloud.app.mjs";

export default {
  key: "sonarcloud-list-organization-options",
  name: "List Organization Options",
  description: "Retrieves available options for the Organization field.",
  version: "0.0.1",
  type: "action",
  annotations: {
    destructiveHint: false,
    openWorldHint: true,
    readOnlyHint: true,
  },
  props: {
    sonarcloud,
    page: {
      type: "integer",
      label: "Page",
      description: "The page of results to retrieve.",
      min: 0,
      default: 0,
    },
  },
  async run({ $ }) {
    const options = await sonarcloud.propDefinitions.organization.options.call(this.sonarcloud, {
      page: this.page,
    });
    $.export("$summary", `Successfully retrieved ${options.length} option${options.length === 1
      ? ""
      : "s"}`);
    return options;
  },
};
