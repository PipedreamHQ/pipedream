import uplisting from "../../uplisting.app.mjs";

export default {
  key: "uplisting-list-property-id-options",
  name: "List Property Id Options",
  description: "Retrieves available options for the Property Id field.",
  version: "0.0.1",
  type: "action",
  annotations: {
    destructiveHint: false,
    openWorldHint: true,
    readOnlyHint: true,
  },
  props: {
    uplisting,
    page: {
      type: "integer",
      label: "Page",
      description: "The page of results to retrieve.",
      min: 0,
      default: 0,
    },
  },
  async run({ $ }) {
    const options = await uplisting.propDefinitions.propertyId.options.call(this.uplisting, {
      page: this.page,
    });
    $.export("$summary", `Successfully retrieved ${options.length} option${options.length === 1
      ? ""
      : "s"}`);
    return options;
  },
};
