import teamgate from "../../teamgate.app.mjs";

export default {
  key: "teamgate-list-person-id-options",
  name: "List PersonId Options",
  description: "Retrieves available options for the PersonId field.",
  version: "0.0.1",
  type: "action",
  annotations: {
    destructiveHint: false,
    openWorldHint: true,
    readOnlyHint: true,
  },
  props: {
    teamgate,
    page: {
      type: "integer",
      label: "Page",
      description: "The page of results to retrieve.",
      min: 0,
      default: 0,
    },
  },
  async run({ $ }) {
    const options = await teamgate.propDefinitions.personId.options.call(this.teamgate, {
      page: this.page,
    });
    $.export("$summary", `Successfully retrieved ${options.length} option${options.length === 1
      ? ""
      : "s"}`);
    return options;
  },
};
