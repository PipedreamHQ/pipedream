import { ConfigurationError } from "@pipedream/platform";
import rydoo from "../../rydoo.app.mjs";

export default {
  key: "rydoo-create-bank-transaction",
  name: "Create Bank Transaction",
  description: "Manually pushes a bank transaction record for a specific user. [See the documentation](https://developers.rydoo.com/reference/v2transactionsaddtransaction)",
  version: "0.0.1",
  type: "action",
  annotations: {
    destructiveHint: false,
    openWorldHint: true,
    readOnlyHint: false,
  },
  props: {
    rydoo,
    userId: {
      propDefinition: [
        rydoo,
        "userId",
      ],
    },
    originalAmount: {
      type: "string",
      label: "Original Amount",
      description: "Transaction amount in the original currency (e.g., `49.99`)",
    },
    originalCurrencyCode: {
      type: "string",
      label: "Original Currency Code",
      description: "ISO 4217 three-letter code of the original currency (e.g., `EUR`, `USD`)",
    },
    billedAmount: {
      type: "string",
      label: "Billed Amount",
      description: "Transaction amount in the billed currency (e.g., `52.10`)",
    },
    billedCurrencyCode: {
      type: "string",
      label: "Billed Currency Code",
      description: "ISO 4217 three-letter code of the billed currency (e.g., `EUR`, `USD`)",
      optional: true,
    },
    paymentMethodId: {
      propDefinition: [
        rydoo,
        "paymentMethodId",
        (c) => ({
          userId: c.userId,
        }),
      ],
      optional: true,
    },
    transactionDate: {
      type: "string",
      label: "Transaction Date",
      description: "Date the transaction occurred in ISO 8601 format (e.g., `2024-06-15T00:00:00Z`)",
    },
    merchantName: {
      propDefinition: [
        rydoo,
        "merchantName",
      ],
    },
    merchantCategoryCode: {
      type: "string",
      label: "Merchant Category Code",
      description: "Four-digit merchant category identifier (e.g., `5812` for restaurants)",
      optional: true,
    },
    processorId: {
      type: "string",
      label: "Processor ID",
      description: "The processor ID generated by the bank",
      optional: true,
    },
    uniqueId: {
      type: "string",
      label: "Unique ID",
      description: "A unique identifier for the transaction from the bank",
    },
    country: {
      type: "string",
      label: "Country",
      description: "3-letter country code where the transaction occurred (e.g., `DEU`, `GBR`, `USA`)",
      optional: true,
    },
    location: {
      type: "string",
      label: "Location",
      description: "Location where the transaction occurred",
      optional: true,
    },
    city: {
      type: "string",
      label: "City",
      description: "City where the transaction occurred",
      optional: true,
    },
  },
  async run({ $ }) {
    const originalAmount = parseFloat(this.originalAmount);
    if (Number.isNaN(originalAmount)) {
      throw new ConfigurationError(`"Original Amount" must be a valid number, got: ${this.originalAmount}`);
    }
    const billedAmount = parseFloat(this.billedAmount);
    if (Number.isNaN(billedAmount)) {
      throw new ConfigurationError(`"Billed Amount" must be a valid number, got: ${this.billedAmount}`);
    }

    const response = await this.rydoo.createTransaction({
      $,
      data: {
        userId: this.userId,
        originalAmount,
        originalCurrencyCode: this.originalCurrencyCode,
        billedAmount,
        billedCurrencyCode: this.billedCurrencyCode,
        paymentMethodId: this.paymentMethodId,
        transactionDate: this.transactionDate,
        merchantName: this.merchantName,
        merchantCategoryCode: this.merchantCategoryCode,
        processorId: this.processorId,
        uniqueId: this.uniqueId,
        country: this.country,
        location: this.location,
        city: this.city,
      },
    });

    $.export("$summary", `Successfully created bank transaction for user ${this.userId} at ${this.merchantName}.`);
    return response;
  },
};
