import telegramBotApi from "../../telegram_bot_api.app.mjs";

export default {
  key: "telegram_bot_api-create-chat-invite-link",
  name: "Create Chat Invite Link",
  description: "Use this method to create an additional invite link for a chat. The bot must be an administrator in the chat for this to work and must have the appropriate administrator rights. The link can be revoked using the method revokeChatInviteLink. Returns the new invite link as ChatInviteLink object. Note: Each administrator in a chat generates their own invite links. Bots can't use invite links generated by other administrators. If you want your bot to work with invite links, it will need to generate its own link using exportChatInviteLink or by calling the getChat method. If your bot needs to generate a new primary invite link replacing its previous one, use exportChatInviteLink again. [See the docs](https://core.telegram.org/bots/api#createchatinvitelink) for more information",
  version: "0.0.1",
  type: "action",
  props: {
    telegramBotApi,
    chatId: {
      propDefinition: [
        telegramBotApi,
        "chatId",
      ],
    },
    name: {
      propDefinition: [
        telegramBotApi,
        "link_name",
      ],
    },
    expire_date: {
      propDefinition: [
        telegramBotApi,
        "expire_date",
      ],
    },
    member_limit: {
      propDefinition: [
        telegramBotApi,
        "member_limit",
      ],
    },
    creates_join_request: {
      propDefinition: [
        telegramBotApi,
        "creates_join_request",
      ],
    },
  },
  async run({ $ }) {
    const resp = await this.telegramBotApi.createChatInviteLink(this.chatId, {
      name: this.name,
      expire_date: this.expire_date,
      member_limit: this.member_limit,
      creates_join_request: this.creates_join_request,
    });
    $.export("$summary", `Successfully created chat invite link, "${this.chatId}"`);
    return resp;
  },
};
