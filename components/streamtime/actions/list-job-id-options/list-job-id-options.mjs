import streamtime from "../../streamtime.app.mjs";

export default {
  key: "streamtime-list-job-id-options",
  name: "List Job Options",
  description: "Retrieves available options for the Job field.",
  version: "0.0.1",
  type: "action",
  annotations: {
    destructiveHint: false,
    openWorldHint: true,
    readOnlyHint: true,
  },
  props: {
    streamtime,
    page: {
      type: "integer",
      label: "Page",
      description: "The page of results to retrieve.",
      min: 0,
      default: 0,
    },
  },
  async run({ $ }) {
    const options = await streamtime.propDefinitions.jobId.options.call(this.streamtime, {
      page: this.page,
    });
    $.export("$summary", `Successfully retrieved ${options.length} option${options.length === 1
      ? ""
      : "s"}`);
    return options;
  },
};
