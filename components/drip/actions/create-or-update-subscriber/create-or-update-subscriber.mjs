    const {
      email,
      visitorUuid,
      newEmail,
      firstName,
      lastName,
      address1,
      address2,
      city,
      state,
      zip,
      country,
      phone,
      smsNumber,
      smsConsent,
      userId,
      timezone,
      lifetimeValue,
      ipAddress,
      customFields,
      tags,
      removeTags,
      prospect,
      baseLeadScore,
      euConsent,
      euConsentMessage,
      status,
      initialStatus,
    } = this;

    const response = await this.drip.createOrUpdateSubscriber({
      $,
      data: {
        subscribers: [
          {
            email: email && email.label
              ? email.label
              : email,
            id: email?.value,
            visitor_uuid: visitorUuid,
            new_email: newEmail,
            first_name: firstName,
            last_name: lastName,
            address1,
            address2,
            city,
            state,
            zip,
            country,
            phone,
            sms_number: smsNumber,
            sms_consent: smsConsent,
            user_id: userId,
            time_zone: timezone,
            lifetime_value: lifetimeValue,
            ip_address: ipAddress,
            custom_fields: customFields,
            tags,
            remove_tags: removeTags,
            prospect,
            base_lead_score: baseLeadScore,
            eu_consent: euConsent,
            eu_consent_message: euConsentMessage,
            status,
            initial_status: initialStatus,
          },
        ],
      },
    });

    $.export("$summary", "Subscriber Successfully created/updated");
