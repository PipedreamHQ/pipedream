import common from "../common/task-props.mjs";

export default {
  ...common,
  key: "clickup-get-task-comments",
  name: "Get Task Comments",
  description: "Get a task comments. [See the documentation](https://clickup.com/api) in **Comments / Get Task Comments** section.",
  version: "0.0.11",
  type: "action",
  props: {
    ...common.props,
    folderId: {
      propDefinition: [
        common.props.clickup,
        "folderId",
        (c) => ({
          spaceId: c.spaceId,
        }),
      ],
      optional: true,
    },
    listId: {
      propDefinition: [
        common.props.clickup,
        "listId",
        (c) => ({
          folderId: c.folderId,
          spaceId: c.spaceId,
        }),
      ],
      optional: true,
    },
    taskId: {
      propDefinition: [
        common.props.clickup,
        "taskId",
        (c) => ({
          listId: c.listId,
          useCustomTaskIds: c.useCustomTaskIds,
          authorizedTeamId: c.authorizedTeamId,
        }),
      ],
      description: "To show options please select a **List** first",
    },
  },
  async run({ $ }) {
    const { taskId } = this;

    const params = this.clickup.getParamsForCustomTaskIdCall(
      this.useCustomTaskIds,
      this.authorizedTeamId,
    );

    const response = await this.clickup.getTaskComments({
      $,
      taskId,
      params,
    });

    $.export("$summary", "Successfully retrieved task comments");

    return response;
  },
};
